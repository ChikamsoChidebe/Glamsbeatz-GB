// Populate PRODUCTS_DATA from products array
products.forEach(product => {
    if (product.type === 'shoes') {
        PRODUCTS_DATA.shoes.push(product);
    } else if (product.type === 'bags') {
        PRODUCTS_DATA.bags.push(product);
    }
});

// Load saved products from localStorage
function loadSavedProducts() {
    const saved = localStorage.getItem('glamsbeatz_products');
    if (saved) {
        const savedData = JSON.parse(saved);
        if (savedData.shoes) PRODUCTS_DATA.shoes = [...savedData.shoes];
        if (savedData.bags) PRODUCTS_DATA.bags = [...savedData.bags];
        // Update main products array
        products.length = 0;
        products.push(...PRODUCTS_DATA.shoes, ...PRODUCTS_DATA.bags);
    }
}

// Save products to localStorage
function saveProductsData() {
    localStorage.setItem('glamsbeatz_products', JSON.stringify(PRODUCTS_DATA));
}

// Initialize on load
if (typeof window !== 'undefined') {
    loadSavedProducts();
}

// Export for use in other files
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { products, PRODUCTS_DATA, loadSavedProducts, saveProductsData };
}